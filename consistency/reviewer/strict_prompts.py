def build_strict_review_prompt(
    findings_data: dict,
    project_name: str,
    commit_sha: str,
    duration: float,
    extra_context: str = "",
) -> str:
    """构建严格的审查提示词"""

    prompt_parts = [
        STRICT_ARCHITECT_SYSTEM_PROMPT,
        "",
        "---",
        "",
        "## 输入数据",
        "",
        f"**项目名称**: {project_name}",
        f"**提交**: {commit_sha}",
        f"**扫描耗时**: {duration:.2f}s",
        "",
        "### 发现的问题数据",
        "```json",
        json.dumps(findings_data, ensure_ascii=False, indent=2),
        "```",
        "",
    ]

    if extra_context:
        prompt_parts.extend([
            "### 额外上下文",
            extra_context,
            "",
        ])

    prompt_parts.extend([
        REVIEW_CHECKLIST,
        "",
        LAYERED_DIAGNOSIS_FORMAT,
        "",
        "---",
        "",
        "## 最终要求",
        "",
        "请严格按上述格式规范输出报告，注意：",
        "1. 先输出顶部概览栏",
        "2. 按 🔴🟠🟢 分组输出问题详情",
        "3. 使用 `<details>` 标签实现可折叠",
        "4. 每个严重/中等问题必须包含：文件路径、代码片段、风险分析、修复方案",
        "5. 使用中文输出",
        "",
        "直接输出 Markdown 内容，不要添加额外说明。",
    ])

    return "\n".join(prompt_parts)


